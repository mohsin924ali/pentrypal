// Sort countries by priority first, then alphabetically
const sortedCountries = COUNTRIES.sort((a, b) => {
  if (a.priority && b.priority) {
    return a.priority - b.priority;
  }
  if (a.priority && !b.priority) {
    return -1;
  }
  if (!a.priority && b.priority) {
    return 1;
  }
  return a.name.localeCompare(b.name);
});

